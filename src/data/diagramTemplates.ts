// カテゴリ別にグループ化
export function getTemplatesByCategory(category: DiagramCategory): DiagramTemplate[] {
  return DIAGRAM_TEMPLATES.filter((t) => t.category === category);
}

// 全カテゴリのリストを取得
export function getAllDiagramCategories(): DiagramCategory[] {
  return Object.keys(DIAGRAM_CATEGORY_LABELS) as DiagramCategory[];
}

// テンプレートをIDで検索
export function getTemplateById(id: string): DiagramTemplate | undefined {
  return DIAGRAM_TEMPLATES.find((t) => t.id === id);
}
